package top.remained.silence.common.exception;

import lombok.Data;
import top.remained.silence.common.result.ResultCodeEnum;

/**
 * @Author: lx
 * @CreateTime: 2023-07-09  22:54
 * @Description: TODO
 */
@Data
public class SilenceException extends RuntimeException{

    //异常状态码
    private Integer code;

    /**
     * 通过状态码和错误消息创建异常对象
     * @param message
     * @param code
     */
    public SilenceException(String message, Integer code) {
        super(message);
        this.code = code;
    }

    /**
     * 接收枚举类型对象
     * @param resultCodeEnum
     */
    public SilenceException(ResultCodeEnum resultCodeEnum) {
        super(resultCodeEnum.getMessage());
        this.code = resultCodeEnum.getCode();
    }

    @Override
    public String toString() {
        return "GuliException{" +
                "code=" + code +
                ", message=" + this.getMessage() +
                '}';
    }
}

